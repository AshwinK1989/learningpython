my_tuple = ('Ashwin',1,2,'Karangutkar','Ashwin','Karangutkar')

#Extracting first value from tuple using indexing
print("The first value from Tuple is :")
print(my_tuple[0])


#Extracting value from 1st position till the end
print("Extracting value from first position till end")
print(my_tuple[1:])

#Getting the number of times a element is present
print(my_tuple.count('Ashwin'))

#Getting the index at which an element is present
print(my_tuple.index('Ashwin'))

